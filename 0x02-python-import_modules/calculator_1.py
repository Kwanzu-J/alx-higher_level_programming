#!/usr/bin/python3

def sub(a, b):
    """
    Subtraction function
    Args:
        a - first integer
        b - second integer
    Returns:
        return value a - b
    """
    return a - b

def add(a, b):
    """
    Addition function
    Args:
        a - first integer
        b - second integer
    Returns:
        return value a + b
    """
    return a + b

def mul(a, b):
    """
    multiplication function
    Args:
        a - first integer
        b - second integer
    Returns:
        return value a * b
    """
    return a * b

def div(a, b):
    """
    Divition function
    Args:
        a - first integer
        b - second integer
    Returns:
        return value a / b
    """
    return a / b
